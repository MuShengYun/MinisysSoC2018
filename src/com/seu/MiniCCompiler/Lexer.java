    public int yylex() throws IOException {
        int state = 45;
        int nextChar;
        yytext = new StringBuilder();
        int lastAccept = -1;
        int lastLength = 0;

        while (true) {

            nextChar = yyin.read();
            if (nextChar == -1)
                if (yytext.toString().isEmpty())
                    return -1;
                else
                    state = -1;
            else {
                state = transitionTable[state][nextChar];
                yytext.append((char) nextChar);
                if (state >= 0 && state <= actionsCount) {
                    lastAccept = state;
                    lastLength = yytext.length();
                }
            }
            if (state == -1) {
                while (yytext.length() > lastLength + ((lastAccept == -1) ? 1 : 0)) {
                    int lastIndex = yytext.toString().length() - 1;
                    yyin.unread(yytext.charAt(lastIndex));
                    yytext.deleteCharAt(lastIndex);
                }
                if (lastAccept == -1)
                    return acceptDefault();
                else try {
                    return (int) Lexer.class.getMethod("accept" + lastAccept).invoke(this);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }


